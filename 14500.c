int n, m, board[504][504];
int main()
{
    scanf("%d %d", &n, &m);
    for (int i = 0; i < n; i++)
        for (int j = 0; j < m; j++)
            scanf("%d", &board[i][j]);

    int max = 0;
    for (int i = 0; i < n; i++)
        for (int j = 0; j < m; j++)
            for (int k = 0; k < 19; k++)
            {
                int sum = 0;
                for (int m = 0; m < 16; m++)
                {
                    sum += board[i + m / 4][j + m % 4] * shapes[k][m / 4][m % 4];
                }
                if (sum > max)
                    max = sum;
            }

    printf("%d\n", max);
    return 0;
}